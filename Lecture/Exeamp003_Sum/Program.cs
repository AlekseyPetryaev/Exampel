//Вводим число = случайное целое число // указываем диапазон .Next(1, 10)
int numberA = new Random().Next(1, 10); // 1 2 3 4 ...9
Console.WriteLine(numberA); //будет показывать в терминеале случайное число
int numberB = new Random().Next(1, 10);
Console.WriteLine(numberB); //будет показывать в терминеале случайное число
int result = numberA + numberB; //дополнительная переменная в которой будет храниться сумма
Console.WriteLine(result); // вывод суммы через переменную 
//6 петвое случайное число 
//3 второе случайное число
//9 сумма первого и второго случайного числа


